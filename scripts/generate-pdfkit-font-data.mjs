/**
 * Generates lib/pdfkitFontData.ts with embedded PDFKit standard font (.afm) data.
 * Run: node scripts/generate-pdfkit-font-data.mjs
 */
import { readFileSync, writeFileSync } from 'fs'
import { createRequire } from 'module'
import { dirname, join } from 'path'
import { fileURLToPath } from 'url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const projectRoot = join(__dirname, '..')

const require = createRequire(import.meta.url)
const pdfkitPath = require.resolve('pdfkit')
const dataDir = join(dirname(pdfkitPath), '..', 'js', 'data')

const FONT_FILES = [
  'Helvetica.afm',
  'Helvetica-Bold.afm',
  'Helvetica-Oblique.afm',
  'Helvetica-BoldOblique.afm',
  'Times-Roman.afm',
  'Times-Bold.afm',
  'Times-Italic.afm',
  'Times-BoldItalic.afm',
  'Courier.afm',
  'Courier-Bold.afm',
  'Courier-Oblique.afm',
  'Courier-BoldOblique.afm',
  'Symbol.afm',
  'ZapfDingbats.afm',
]

const entries = []
for (const fileName of FONT_FILES) {
  const filePath = join(dataDir, fileName)
  const content = readFileSync(filePath, 'utf8')
  const escaped = JSON.stringify(content)
  entries.push(`  ${JSON.stringify(fileName)}: Buffer.from(${escaped}, 'utf8'),`)
}

const output = `/**
 * AUTO-GENERATED by scripts/generate-pdfkit-font-data.mjs — do not edit manually.
 * Re-run: pnpm generate:pdf-fonts
 *
 * Embedded PDFKit standard font data for serverless/edge runtimes (Cloudflare Workers)
 * where node_modules/pdfkit/js/data is not available on disk at runtime.
 */
export const PDFKIT_FONT_DATA: Record<string, Buffer> = {
${entries.join('\n')}
}
`

const outPath = join(projectRoot, 'lib', 'pdfkitFontData.ts')
writeFileSync(outPath, output, 'utf8')
console.log(`Wrote ${outPath} (${FONT_FILES.length} fonts)`)
